// BIT MASKS
#include<vector>
#include <iostream>
#include<set>
#include<algorithm>
#include<cmath>
#define ll long long
#define ld long double
using namespace std;
int main() {
	int t = 1; //cin >> t;
	while (t--) {
		ll n, m, k; cin >> n >> m >> k;
		vector<int>v(m);

		for (int i = 0; i < m; i++) {
			cin >> v[i];
		}
		ll fedor; cin >> fedor;
		set<int>s;
		for (int i = 0; i < 31; i++) {
			if ((fedor >> i) & 1) {
				s.insert(i);
			}
		}
		int ans = 0;
		for (int i = 0; i < m; i++) {
			int cnt = 0;
			for (int j = 0; j < 31; j++) {
				int a = ((v[i] >> j) & 1), b = ((fedor >> j) & 1);
				if (a!=b) {
					cnt++;
				}
			}
			if (cnt <= k)ans++;

		}
		cout << ans;
		cout << '\n';
	}
}
